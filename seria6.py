# Шаг 1: Определяем класс `Dog`, который будет использоваться для создания объектов (экземпляров) собак.
# Классы в Python определяют шаблон для создания объектов с определенными свойствами и поведением.
class Dog:
    # Шаг 2: Определяем метод-конструктор `__init__`, который вызывается при создании нового объекта класса `Dog`.
    # Этот метод принимает три аргумента: `self` (ссылка на сам объект), `name` (имя собаки) и `age` (возраст собаки).
    def __init__(self, name, age):
        # Шаг 3: Присваиваем значения аргументов `name` и `age` свойствам объекта (`self.name` и `self.age`).
        # `self.name` и `self.age` создают атрибуты объекта, которые будут хранить имя и возраст конкретной собаки.
        self.name = name
        self.age = age

    # Шаг 4: Определяем метод `bark`, который будет доступен всем экземплярам класса `Dog`.
    # Этот метод не принимает дополнительных аргументов, кроме `self`, и просто выводит текст "Гав! Гав!".
    def bark(self):
        print("Гав! Гав!")  # Этот метод выводит звук, который издает собака

# Шаг 5: Создаем объект `dog` класса `Dog`, передавая в конструктор имя "Бобик" и возраст 10.
# При создании объекта вызывается метод `__init__`, который инициализирует атрибуты `name` и `age` для этого объекта.
dog = Dog("Бобик", 10)

# Шаг 6: Вызываем метод `bark` для объекта `dog`.
# Этот метод принадлежит объекту и выводит строку "Гав! Гав!" на экран.
dog.bark()  # Объект издает звук "Гав! Гав!"
