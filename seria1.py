# Шаг 1: Объявляем переменную `name` и присваиваем ей значение "Monday"
# `name` будет хранить строку, которая обозначает название дня недели.
name = "Monday"  # Название дня недели

# Шаг 2: Объявляем переменную `number` и присваиваем ей значение 1
# `number` будет хранить целое число, которое обозначает номер дня недели.
number = 1  # Номер дня недели

# Шаг 3: Используем функцию `print`, чтобы вывести на экран значения переменных `name` и `number`.
# Функция `print` принимает несколько аргументов и выводит их значения через пробел.
print(name, number)
