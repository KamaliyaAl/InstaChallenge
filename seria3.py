# Шаг 1: Создаём список `items`, который содержит три строки: 'milk', 'bread', 'cookies'.
# Списки в Python — это упорядоченные коллекции, в которых элементы могут быть доступны по индексу.
items = ['milk', 'bread', 'cookies']

# Шаг 2: Создаём список `numbers`, который содержит числа от 1 до 7.
numbers = [1, 2, 3, 4, 5, 6, 7]

# Шаг 3: Выводим первый элемент списка `items` с индексом 0.
# В Python индексация списков начинается с 0, поэтому `items[0]` обращается к первому элементу списка.
print(items[0])  # milk

# Шаг 4: Выводим второй элемент списка `items` с индексом 1.
print(items[1])  # bread

# Шаг 5: Выводим третий элемент списка `items` с индексом 2.
print(items[2])  # cookies

# Шаг 6: Выводим весь список `items` целиком.
# Когда мы просто передаём список в функцию `print`, он выводится как один объект, и Python покажет его в виде строки со всеми элементами внутри квадратных скобок.
print(items)  # ['milk', 'bread', 'cookies']

# Шаг 7: Используем оператор `*` (звездочка), чтобы распаковать элементы списка `items`.
# Оператор `*` распаковывает элементы списка и передаёт их как отдельные аргументы в функцию `print`.
# Параметр `sep=", "` задаёт разделитель между элементами, в данном случае запятая и пробел.
print(*items, sep=", ")  # milk, bread, cookies

# Шаг 8: Используем функцию `len`, чтобы получить длину списка `numbers`, то есть количество элементов в списке.
# `len(numbers)` возвращает число 7, так как в списке `numbers` содержится 7 элементов.
print(len(numbers))  # 7
